    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // Evento de el Boton de Agregar
        //Preguntar asignaciones que se requieren datos de usuario
        String Nombre; float SB;
        
         Nombre = JOptionPane.showInputDialog("Nombre");
         
         //El Nombre del usuario es obligatorio si no lo ingresa cancelar la operaci√≥n inmediatamente
         if (Nombre == null)
         {
             return;    //Si el Nombre no fue ingresado retornar a nada
         }
         if (Nombre.trim().length()==0)
         {
             return;    //Si se ingreso un Nombre como cadena de espacios vacios retornar a nada
         }
         //Si se llego hasta aqui el usuario ingreso de manera correcta su nombre 
         /*--------------------------------------------------------------------------------------------*/
         //Conversion a numero de Salario Neto (SN) si falla la conversi√≥n no continuar
         try {
            SB = Float.parseFloat(JOptionPane.showInputDialog("Salario Bruto"));
            
        } catch (Exception e) {
            return;      //Si falla no continua
        }
         //Si ha llegado hasta aqui tanto el Nombre como el Salario Neto se han ingresado de manera correcta
         /*-----------------------------------------------------------------------------------------------------------*/
         //Hacer los Calculos 
         float IHSSENFMAT = 0.00f;  //Calcular el IHSS: ENF-MAT
         if (SB >= 8933.97)
         {
             IHSSENFMAT = (8933.97f * 2.5f)/100; 
         }
         else 
         {
             IHSSENFMAT = (SB * 2.5f)/100;
         } 
         
         float IHSSIVM = 0.00f;   //Calcular el IHSS: IVM
         if (SB >= 9326.42) 
         {
            IHSSIVM = (9326.42f * 2.5f)/100;
         }
         else
         {
            IHSSIVM = (SB * 2.5f)/100;
         }
         
         
         float RAP;
         {
             RAP = (SB*1.5f)/100;
         }
         
         float SN = 0.00f;
         SN = (SB - IHSSENFMAT - IHSSIVM - RAP);
         
         //Crear la Fila en la Tabla (JTable)
         Vector fila = new Vector();
         fila.add(Nombre);
         fila.add(SB);
         fila.add(IHSSENFMAT);
         fila.add(IHSSIVM);
         fila.add(RAP);
         fila.add(SN);
         asignaciones.addRow(fila);
         
        sumarios ();
         
    }//GEN-LAST:event_addActionPerformed

    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        // Evento de el Boton de Eliminar
        //Obtener la posici√≥n de la fila
        int posicion;
        posicion = tabla.getSelectedRow();
        //si posicion es -1, no ha seleccionado una fila para borrar, por lo tanto no continuar
        if( posicion == -1 )
            return;
        //si llego hasta aqui entonces borrar
        asignaciones.removeRow(posicion);
        
        sumarios();
    }//GEN-LAST:event_delActionPerformed

