    //Metodo creado para configurar la seccion de Sumarios
    public void sumarios()
    {
        Vector latabla;
        latabla = asignaciones.getDataVector(); //convierte la asigancion de la tabla a vector
        //el vector lamadado "latabla" contiene a las fias del jtable las cuales era peque√±os vectores 
        //recorremos el vector "latabla" fila por fila
        float sumaSB = 0.00f; //variable acumuladora de  los salarios brutos
        float sumaIHSSENFMAT = 0.00f; //variable acumuladora de IHSS:ENF-MAT
        float sumaIHSSIVM = 0.00f; //variable acumuladora de IHSSIVM
        float sumaRAP = 0.00f; //variable acumuladora de RAP
        float sumaSN = 0.00f; //variable acumuladora de los salarios netos
        
        Vector fila; //Variable temporal para ir recuperando cada fila
        for (int i = 0;i<latabla.size();i++)
        {
            //recuperar la fila almacenada en e vector "latabla" 
            //al recuperar la fila es necesario CONVERTIRLA A VECTOR y ponerla en una variable temporal
            fila = (Vector) latabla.elementAt(i);
            //las columnas se enumeran de 0 a N-1
            //EJEMPLO: la columna 0 es la columna de Nombre
            //la columna 1 es la columna Salario Bruto (SB)
            //la columna 2 es la columna IHSS: ENF-MAT
            //la columna 3 es la columna IHSS: IVM
            //la columna 4 es la colmna RAP
            //la columna 5 es la columna de Salario Neto
            
            //Sumar todos los valores de la columna Salario Bruto 
            sumaSB = sumaSB + Float.parseFloat(fila.elementAt(1).toString());
            
            //Sumar todos los valores de la columna IHSS:ENF-MAT 
            sumaIHSSENFMAT = sumaIHSSENFMAT + Float.parseFloat(fila.elementAt(2).toString());
            
            //Sumar todos los valores de la columna IHSS:IVM
            sumaIHSSIVM = sumaIHSSIVM + Float.parseFloat(fila.elementAt(3).toString());
            
            //Sumar todos los valores de la columna RAP 
            sumaRAP = sumaRAP + Float.parseFloat(fila.elementAt(4).toString());
            
            //Sumar todos los valores de la columna Salario Neto 
            sumaSN = sumaSN + Float.parseFloat(fila.elementAt(5).toString());
        }
        //Terminando el FOR colocamos la suma de los Salarios Brutos
        DecimalFormat df = new  DecimalFormat("#.00"); 
        
        //Aplicar el formato a dos decimales al poner el texto en cada caja
        SB.setText( df.format(sumaSB) );
        ENFMAT.setText( df.format(sumaIHSSENFMAT) );
        IVM.setText( df.format(sumaIHSSIVM) );
        RAP.setText( df.format(sumaRAP));
        SN.setText( df.format(sumaSN));
    }
    
